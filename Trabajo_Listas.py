Trabajadores=["Pedro","Juan","Lecko","Hans", "Michael"] ##lista 

Trabajadores.append("Rogers") #lista con metodo que agrega nuevos elementos

print(len(Trabajadores)) #len es una funcion que devuelve la cantidad de elementos de la lista

print(Trabajadores) #imprime en consola la lista

Trabajadores.remove("Pedro") #remove elimina elementos de la lista

print(Trabajadores[4]) #muestra el elemento que esta en la posicion 4

print(Trabajadores[-2]) #muestra el elemento en esa posicion contando desde el ultimo elemento de la lista

print(Trabajadores[1:5]) #muestra todos los elementos dentro de ese rango de posiciones

